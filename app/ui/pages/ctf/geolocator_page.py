def _records(window) -> list[Any]:
    return list(getattr(window.case_manager, "records", []) or [])


def _top_record(records: list[Any]) -> Any | None:
    if not records:
        return None
    return sorted(
        records,
        key=lambda record: (-int(getattr(record, "location_solvability_score", 0) or 0), getattr(record, "evidence_id", "")),
    )[0]


def _candidate_key(record: Any, candidate: dict[str, Any]) -> str:
    return "|".join(
        [
            str(getattr(record, "evidence_id", "EV")),
            str(candidate.get("name", "")),
            str(candidate.get("level", "")),
            str(candidate.get("basis", [])),
        ]
    )


def _candidate_status_rank(candidate: dict[str, Any]) -> int:
    return {"verified": 0, "needs_review": 1, "rejected": 9}.get(str(candidate.get("status", "needs_review")), 5)


def _iter_candidates(records: list[Any]) -> list[tuple[Any, dict[str, Any], str]]:
    rows: list[tuple[Any, dict[str, Any], str]] = []
    for record in sorted(records, key=lambda item: (-int(getattr(item, "location_solvability_score", 0) or 0), getattr(item, "evidence_id", ""))):
        for candidate in getattr(record, "geo_candidates", []) or []:
            if isinstance(candidate, dict):
                rows.append((record, candidate, _candidate_key(record, candidate)))
    rows.sort(
        key=lambda row: (
            _candidate_status_rank(row[1]),
            -int(row[1].get("confidence", 0) or 0),
            str(row[0].evidence_id if hasattr(row[0], "evidence_id") else ""),
            str(row[1].get("name", "")).lower(),
        )
    )
    return rows


def _selected_candidate_key(window) -> str:
    table = getattr(window, "ctf_candidate_table", None)
    if table is not None:
        selected = table.selectedItems()
        if selected:
            key = selected[0].data(Qt.UserRole)
            if key:
                return str(key)
    return str(getattr(window, "ctf_selected_candidate_key", "") or "")


def _find_candidate_by_key(records: list[Any], key: str) -> tuple[Any | None, dict[str, Any] | None]:
    if key:
        for record, candidate, candidate_key in _iter_candidates(records):
            if candidate_key == key:
                return record, candidate
    for record, candidate, _ in _iter_candidates(records):
        if candidate.get("status", "needs_review") == "needs_review":
            return record, candidate
    return None, None


def _update_candidate_status_by_key(window, key: str, status: str, note: str = "") -> bool:
    record, candidate = _find_candidate_by_key(_records(window), key)
    if record is None or candidate is None:
        return False
    candidate["status"] = status
    candidate["analyst_updated_at"] = datetime.now(timezone.utc).isoformat(timespec="seconds")
    if note:
        candidate["analyst_note"] = note
    elif status == "verified":
        candidate["analyst_note"] = "Analyst marked this selected candidate as verified after manual review."
    elif status == "rejected":
        candidate["analyst_note"] = "Analyst rejected this selected candidate as unsupported/noisy."
    else:
        candidate["analyst_note"] = "Analyst reset this selected candidate for review."
    setattr(window, "ctf_selected_candidate_key", _candidate_key(record, candidate))
    if hasattr(window.case_manager, "_write_case_snapshot"):
        window.case_manager._write_case_snapshot()
    return True


def _update_selected_candidate(window, status: str) -> None:
    key = _selected_candidate_key(window)
    updated = _update_candidate_status_by_key(window, key, status)
    refresh_ctf_geolocator_page(window)
    if hasattr(window, "_show_toast"):
        window._show_toast(
            "CTF GeoLocator",
            f"Selected candidate marked {status}." if updated else "No candidate was available for review.",
            tone="info" if updated else "warning",
        )


def _handle_candidate_selection(window) -> None:
    setattr(window, "ctf_selected_candidate_key", _selected_candidate_key(window))
    refresh_ctf_geolocator_page(window)


def _generate_search_plan(window) -> None:
    refresh_ctf_geolocator_page(window)
    if hasattr(window, "_show_toast"):
        window._show_toast("OSINT search plan", "Manual search pivots refreshed from current CTF clues.", tone="success")


def _record_for_ctf_action(window) -> Any | None:
    records = _records(window)
    record, _candidate = _find_candidate_by_key(records, _selected_candidate_key(window))
    if record is not None:
        return record
    try:
        selected = window.selected_record()
        if selected is not None:
            return selected
    except Exception as exc:
        logger = getattr(getattr(window, "case_manager", None), "logger", None)
        if logger is not None:
            logger.debug("CTF action selected_record fallback failed: %s", exc)
    return _top_record(records)


def _refresh_ctf_related_views(window) -> None:
    refresh_ctf_geolocator_page(window)
    if hasattr(window, "refresh_ai_guardian"):
        window.refresh_ai_guardian()
    elif hasattr(window, "refresh_dashboard"):
        window.refresh_dashboard()


def _enable_deep_map_ocr(window) -> None:
    os.environ["GEOTRACE_OCR_MODE"] = "map_deep"
    setattr(window, "osint_active_ocr_profile", "map_deep")
    record = _record_for_ctf_action(window)
    if record is None or not hasattr(window.case_manager, "rescan_record_osint"):
        if hasattr(window, "_show_toast"):
            window._show_toast("Deep Map OCR", "map_deep will be used on the next scan/import. No selected evidence item was available for immediate rescan.", tone="warning")
        refresh_ctf_geolocator_page(window)
        return
    updated = window.case_manager.rescan_record_osint(str(getattr(record, "evidence_id", "")), mode="map_deep", force=True)
    if hasattr(window, "_show_toast"):
        if updated is not None:
            window._show_toast("Deep Map OCR", f"Rescanned {getattr(updated, 'evidence_id', 'evidence')} and refreshed OCR/map/CTF signals.", tone="success")
        else:
            window._show_toast("Deep Map OCR", "Could not rescan the selected evidence. Check that the staged file still exists.", tone="warning")
    _refresh_ctf_related_views(window)


