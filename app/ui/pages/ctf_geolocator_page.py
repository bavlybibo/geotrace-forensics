from __future__ import annotations

"""OSINT-owned CTF GeoLocator page.

This page visualises the CTF/GeoGuessr-style layer generated by app.core.osint.
It does not call external services. Search queries are manual pivots for authorised use.
"""

from datetime import datetime, timezone
from html import escape
from pathlib import Path
from typing import Any
import os

from PyQt5.QtCore import Qt
from PyQt5.QtWidgets import (
    QAbstractItemView,
    QFrame,
    QGridLayout,
    QHBoxLayout,
    QHeaderView,
    QLabel,
    QPushButton,
    QTableWidget,
    QTableWidgetItem,
    QVBoxLayout,
    QWidget,
)


def _records(window) -> list[Any]:
    return list(getattr(window.case_manager, "records", []) or [])


def _top_record(records: list[Any]) -> Any | None:
    if not records:
        return None
    return sorted(
        records,
        key=lambda record: (-int(getattr(record, "location_solvability_score", 0) or 0), getattr(record, "evidence_id", "")),
    )[0]


def _candidate_key(record: Any, candidate: dict[str, Any]) -> str:
    return "|".join(
        [
            str(getattr(record, "evidence_id", "EV")),
            str(candidate.get("name", "")),
            str(candidate.get("level", "")),
            str(candidate.get("basis", [])),
        ]
    )


def _candidate_status_rank(candidate: dict[str, Any]) -> int:
    return {"verified": 0, "needs_review": 1, "rejected": 9}.get(str(candidate.get("status", "needs_review")), 5)


def _iter_candidates(records: list[Any]) -> list[tuple[Any, dict[str, Any], str]]:
    rows: list[tuple[Any, dict[str, Any], str]] = []
    for record in sorted(records, key=lambda item: (-int(getattr(item, "location_solvability_score", 0) or 0), getattr(item, "evidence_id", ""))):
        for candidate in getattr(record, "geo_candidates", []) or []:
            if isinstance(candidate, dict):
                rows.append((record, candidate, _candidate_key(record, candidate)))
    rows.sort(
        key=lambda row: (
            _candidate_status_rank(row[1]),
            -int(row[1].get("confidence", 0) or 0),
            str(row[0].evidence_id if hasattr(row[0], "evidence_id") else ""),
            str(row[1].get("name", "")).lower(),
        )
    )
    return rows


def _selected_candidate_key(window) -> str:
    table = getattr(window, "ctf_candidate_table", None)
    if table is not None:
        selected = table.selectedItems()
        if selected:
            key = selected[0].data(Qt.UserRole)
            if key:
                return str(key)
    return str(getattr(window, "ctf_selected_candidate_key", "") or "")


def _find_candidate_by_key(records: list[Any], key: str) -> tuple[Any | None, dict[str, Any] | None]:
    if key:
        for record, candidate, candidate_key in _iter_candidates(records):
            if candidate_key == key:
                return record, candidate
    for record, candidate, _ in _iter_candidates(records):
        if candidate.get("status", "needs_review") == "needs_review":
            return record, candidate
    return None, None


def _update_candidate_status_by_key(window, key: str, status: str, note: str = "") -> bool:
    record, candidate = _find_candidate_by_key(_records(window), key)
    if record is None or candidate is None:
        return False
    candidate["status"] = status
    candidate["analyst_updated_at"] = datetime.now(timezone.utc).isoformat(timespec="seconds")
    if note:
        candidate["analyst_note"] = note
    elif status == "verified":
        candidate["analyst_note"] = "Analyst marked this selected candidate as verified after manual review."
    elif status == "rejected":
        candidate["analyst_note"] = "Analyst rejected this selected candidate as unsupported/noisy."
    else:
        candidate["analyst_note"] = "Analyst reset this selected candidate for review."
    setattr(window, "ctf_selected_candidate_key", _candidate_key(record, candidate))
    if hasattr(window.case_manager, "_write_case_snapshot"):
        window.case_manager._write_case_snapshot()
    return True


def _update_selected_candidate(window, status: str) -> None:
    key = _selected_candidate_key(window)
    updated = _update_candidate_status_by_key(window, key, status)
    refresh_ctf_geolocator_page(window)
    if hasattr(window, "_show_toast"):
        window._show_toast(
            "CTF GeoLocator",
            f"Selected candidate marked {status}." if updated else "No candidate was available for review.",
            tone="info" if updated else "warning",
        )


def _generate_search_plan(window) -> None:
    refresh_ctf_geolocator_page(window)
    if hasattr(window, "_show_toast"):
        window._show_toast("OSINT search plan", "Manual search pivots refreshed from current CTF clues.", tone="success")


def _enable_deep_map_ocr(window) -> None:
    os.environ["GEOTRACE_OCR_MODE"] = "map_deep"
    setattr(window, "osint_active_ocr_profile", "map_deep")
    if hasattr(window, "_show_toast"):
        window._show_toast("Deep Map OCR", "map_deep OCR will be used on the next scan/import. Re-import or rescan the evidence to extract labels.", tone="info")
    refresh_ctf_geolocator_page(window)


def _extract_map_labels_hint(window) -> None:
    os.environ["GEOTRACE_OCR_MODE"] = "map_deep"
    if hasattr(window, "_show_toast"):
        window._show_toast("Extract Map Labels", "Map-label extraction is prepared. Re-import/rescan with OCR: map_deep to refresh OCR labels and region-aware map signals.", tone="info")
    refresh_ctf_geolocator_page(window)


def _export_ctf_writeup(window) -> None:
    records = _records(window)
    if not records:
        return
    case_id = getattr(window.case_manager, "active_case_id", "case") or "case"
    base = Path(getattr(window.case_manager, "case_root", Path("case_data"))) / case_id / "exports"
    base.mkdir(parents=True, exist_ok=True)
    path = base / f"ctf_geolocator_writeup_{datetime.now().strftime('%Y%m%d_%H%M%S')}.md"
    sections: list[str] = ["# GeoTrace OSINT / CTF GeoLocator Writeup", ""]
    for record in sorted(records, key=lambda item: (-int(getattr(item, "location_solvability_score", 0) or 0), getattr(item, "evidence_id", ""))):
        sections.append(str(getattr(record, "ctf_writeup", "") or f"No CTF writeup generated for {getattr(record, 'evidence_id', 'evidence')}." ).strip())
        candidates = getattr(record, "geo_candidates", []) or []
        reviewed = [c for c in candidates if isinstance(c, dict) and c.get("status") in {"verified", "rejected"}]
        if reviewed:
            sections.append("\n## Analyst candidate notes")
            for candidate in reviewed:
                sections.append(
                    f"- {candidate.get('status', 'needs_review').upper()}: {candidate.get('name', 'candidate')} "
                    f"({candidate.get('level', 'level')}, {candidate.get('confidence', 0)}%) — "
                    f"{candidate.get('analyst_note', 'No note.')}"
                )
        privacy = getattr(record, "ctf_online_privacy_review", {}) or {}
        if privacy:
            sections.append("\n## Online-search privacy gate")
            sections.append(f"- Required before online search: {privacy.get('required_before_online_search', True)}")
            sections.append(f"- Blocked by default: {', '.join(privacy.get('blocked_by_default', []))}")
        sections.append("\n---\n")
    path.write_text("\n".join(sections).strip() + "\n", encoding="utf-8")
    if hasattr(window, "export_summary"):
        existing = window.export_summary.toPlainText().strip()
        window.export_summary.setPlainText(((existing + "\n") if existing else "") + f"CTF GeoLocator writeup exported: {path}")
    if hasattr(window, "_show_toast"):
        window._show_toast("CTF writeup exported", path.name, tone="success")
    refresh_ctf_geolocator_page(window)


def _shell(window, title: str, attr: str, placeholder: str, subtitle: str, height: int) -> QWidget:
    view = window._make_guardian_view(placeholder, height)
    setattr(window, attr, view)
    return window._shell(title, view, subtitle)


def _table_shell(title: str, table: QTableWidget, subtitle: str) -> QWidget:
    frame = QFrame()
    frame.setObjectName("PanelFrame")
    layout = QVBoxLayout(frame)
    layout.setContentsMargins(14, 14, 14, 14)
    layout.setSpacing(8)
    label = QLabel(title)
    label.setObjectName("SectionLabel")
    meta = QLabel(subtitle)
    meta.setObjectName("SectionMetaLabel")
    meta.setWordWrap(True)
    layout.addWidget(label)
    layout.addWidget(meta)
    layout.addWidget(table)
    return frame


def _render_clue_cards(records: list[Any]) -> str:
    blocks: list[str] = []
    for record in records:
        evidence_id = escape(str(getattr(record, "evidence_id", "EV")))
        score = int(getattr(record, "location_solvability_score", 0) or 0)
        label = escape(str(getattr(record, "location_solvability_label", "No useful geo clue")))
        header_added = False
        for clue in (getattr(record, "ctf_clues", []) or [])[:12]:
            if not header_added:
                blocks.append(
                    f"<div style='margin:0 0 8px 0;color:#a9bfd3;'><b>{evidence_id}</b> • Location solvability: <b>{score}%</b> — {label}</div>"
                )
                header_added = True
            clue_type = escape(str(clue.get("clue_type", "clue")))
            value = escape(str(clue.get("value", ""))[:150])
            source = escape(str(clue.get("source", "unknown")))
            confidence = escape(str(clue.get("confidence", 0)))
            strength = escape(str(clue.get("evidence_strength", "weak_signal")))
            why = escape(str(clue.get("why_it_matters", ""))[:220])
            blocks.append(
                "<div style='border:1px solid #2b4054;border-radius:12px;padding:10px;margin-bottom:8px;background:#0f1822;'>"
                f"<b>{clue_type.upper()}</b> • {value}<br/>"
                f"<span style='color:#9fb2c7;'>Source: {source} • Confidence: {confidence}% • Strength: {strength}</span><br/>"
                f"<span style='color:#91a7bd;'>{why}</span>"
                "</div>"
            )
    return "".join(blocks) if blocks else "<p>No CTF clues are available yet. Import/analyze evidence first.</p>"


def _populate_candidate_table(window, records: list[Any]) -> None:
    table: QTableWidget = window.ctf_candidate_table
    rows = _iter_candidates(records)
    table.setRowCount(len(rows))
    headers = ["Evidence", "Candidate", "Level", "Confidence", "Strength", "Status", "Basis"]
    table.setColumnCount(len(headers))
    table.setHorizontalHeaderLabels(headers)
    selected_key = _selected_candidate_key(window)
    selected_row = 0 if rows else -1
    for row, (record, candidate, key) in enumerate(rows):
        values = [
            str(getattr(record, "evidence_id", "EV")),
            str(candidate.get("name", "")),
            str(candidate.get("level", "")),
            f"{candidate.get('confidence', 0)}%",
            str(candidate.get("evidence_strength", "weak_signal")),
            str(candidate.get("status", "needs_review")),
            ", ".join(str(x) for x in candidate.get("basis", [])[:3]) or "n/a",
        ]
        for col, value in enumerate(values):
            item = QTableWidgetItem(value)
            item.setData(Qt.UserRole, key)
            item.setFlags(item.flags() & ~Qt.ItemIsEditable)
            table.setItem(row, col, item)
        if key == selected_key:
            selected_row = row
    table.resizeColumnsToContents()
    if rows and selected_row >= 0:
        table.selectRow(selected_row)
        setattr(window, "ctf_selected_candidate_key", rows[selected_row][2])


def _render_search_queries(records: list[Any]) -> str:
    lines: list[str] = []
    seen: set[str] = set()
    for record in records:
        for query in getattr(record, "ctf_search_queries", []) or []:
            key = str(query).lower()
            if key in seen:
                continue
            seen.add(key)
            lines.append(f"- {query}")
            if len(lines) >= 40:
                break
    if not lines:
        return "No manual OSINT search pivots generated yet."
    return "Manual, authorised search pivots only — do not upload private evidence without privacy approval.\n\n" + "\n".join(lines)


def _render_metadata(records: list[Any]) -> str:
    lines: list[str] = []
    for record in records[:30]:
        existence = getattr(record, "ctf_image_existence_profile", {}) or {}
        privacy = getattr(record, "ctf_online_privacy_review", {}) or {}
        lines.append(
            f"{getattr(record, 'evidence_id', 'EV')}: solvability={getattr(record, 'location_solvability_score', 0)}% "
            f"({getattr(record, 'location_solvability_label', 'No useful geo clue')}) | "
            f"region={getattr(record, 'ctf_country_region_profile', 'Unknown')} | "
            f"filename_hints={', '.join(getattr(record, 'filename_location_hints', [])[:3]) or 'none'} | "
            f"exact_duplicate={existence.get('exact_duplicate_in_case', False)} | "
            f"near_duplicate={existence.get('near_duplicate_in_case', False)} | "
            f"landmark_match={existence.get('known_landmark_match', False)} | "
            f"privacy_gate={privacy.get('required_before_online_search', True)}"
        )
        ladder = getattr(record, "map_evidence_ladder", []) or []
        if ladder:
            lines.append("  Map Evidence Ladder:")
            lines.extend(f"  - {item}" for item in ladder[:8])
    return "\n".join(lines) if lines else "No CTF metadata rows available yet."


def _render_writeup(records: list[Any]) -> str:
    record = _top_record(records)
    if record is None:
        return "No active evidence item for a CTF writeup yet."
    evidence_id = getattr(record, "evidence_id", "evidence")
    score = getattr(record, "location_solvability_score", 0)
    label = getattr(record, "location_solvability_label", "No useful geo clue")
    candidates = [c for c in (getattr(record, "geo_candidates", []) or []) if isinstance(c, dict)]
    active = [c for c in candidates if c.get("status", "needs_review") != "rejected"]
    rejected = [c for c in candidates if c.get("status") == "rejected"]
    lines = [f"CTF GeoLocator live writeup for {evidence_id}", "=" * (32 + len(str(evidence_id))), "", f"Location solvability: {label} ({score}%)", ""]
    lines.append("Active candidates:")
    if active:
        for candidate in active[:5]:
            lines.append(
                f"- {candidate.get('name', 'candidate')} [{candidate.get('level', 'level')}] — "
                f"{candidate.get('confidence', 0)}% — {candidate.get('evidence_strength', 'weak_signal')} — "
                f"status={candidate.get('status', 'needs_review')}"
            )
    else:
        lines.append("- No active candidate remains. Rejected candidates are not used as top candidates.")
    if rejected:
        lines.extend(["", "Rejected candidates:"])
        for candidate in rejected[:5]:
            lines.append(f"- {candidate.get('name', 'candidate')} — {candidate.get('analyst_note', 'Rejected by analyst.')}")
    clues = getattr(record, "ctf_clues", []) or []
    if clues:
        lines.extend(["", "Key clues:"])
        for clue in clues[:8]:
            if isinstance(clue, dict):
                lines.append(f"- {clue.get('clue_type', 'clue')}: {clue.get('value', '')} ({clue.get('source', 'unknown')}, {clue.get('confidence', 0)}%)")
    ladder = getattr(record, "map_evidence_ladder", []) or []
    if ladder:
        lines.extend(["", "Map Evidence Ladder:"])
        lines.extend(f"- {item}" for item in ladder[:8])
    queries = getattr(record, "ctf_search_queries", []) or []
    if queries:
        lines.extend(["", "Manual search pivots:"])
        lines.extend(f"- {query}" for query in queries[:8])
    lines.extend(["", "Privacy note:", "- Offline by default. Do not upload image content or run external/reverse searches without explicit privacy approval."])
    return "\n".join(lines)


def build_ctf_geolocator_page(window) -> QWidget:
    widget = QWidget()
    layout = QVBoxLayout(widget)
    layout.setContentsMargins(0, 0, 0, 0)
    layout.setSpacing(14)

    hero = QFrame()
    hero.setObjectName("PanelFrame")
    hero_layout = QVBoxLayout(hero)
    hero_layout.setContentsMargins(16, 16, 16, 16)
    hero_layout.setSpacing(10)

    title = QLabel("OSINT Workbench • CTF GeoLocator")
    title.setObjectName("SectionLabel")
    meta = QLabel(
        "CTF-style geolocation layer inside OSINT: clue cards, location solvability, ranked candidates, "
        "manual search pivots, candidate review notes, image-existence intelligence, and privacy-gated online workflows."
    )
    meta.setObjectName("SectionMetaLabel")
    meta.setWordWrap(True)
    hero_layout.addWidget(title)
    hero_layout.addWidget(meta)

    controls = QHBoxLayout()
    controls.setSpacing(8)
    window.btn_ctf_generate_plan = QPushButton("Generate OSINT Search Plan")
    window.btn_ctf_generate_plan.clicked.connect(lambda: _generate_search_plan(window))
    window.btn_ctf_verify = QPushButton("Mark Candidate Verified")
    window.btn_ctf_verify.clicked.connect(lambda: _update_selected_candidate(window, "verified"))
    window.btn_ctf_reject = QPushButton("Reject Candidate")
    window.btn_ctf_reject.clicked.connect(lambda: _update_selected_candidate(window, "rejected"))
    window.btn_ctf_deep_ocr = QPushButton("Run Deep Map OCR")
    window.btn_ctf_deep_ocr.clicked.connect(lambda: _enable_deep_map_ocr(window))
    window.btn_ctf_extract_labels = QPushButton("Extract Map Labels")
    window.btn_ctf_extract_labels.clicked.connect(lambda: _extract_map_labels_hint(window))
    window.btn_ctf_export = QPushButton("Export CTF Writeup")
    window.btn_ctf_export.clicked.connect(lambda: _export_ctf_writeup(window))
    for btn in [window.btn_ctf_generate_plan, window.btn_ctf_deep_ocr, window.btn_ctf_extract_labels, window.btn_ctf_verify, window.btn_ctf_reject, window.btn_ctf_export]:
        controls.addWidget(btn)
    controls.addStretch(1)
    hero_layout.addLayout(controls)
    layout.addWidget(hero)

    window.ctf_candidate_table = QTableWidget()
    window.ctf_candidate_table.setMinimumHeight(360)
    window.ctf_candidate_table.setSelectionBehavior(QAbstractItemView.SelectRows)
    window.ctf_candidate_table.setSelectionMode(QAbstractItemView.SingleSelection)
    window.ctf_candidate_table.verticalHeader().setVisible(False)
    window.ctf_candidate_table.horizontalHeader().setSectionResizeMode(QHeaderView.ResizeToContents)
    window.ctf_candidate_table.horizontalHeader().setStretchLastSection(True)
    window.ctf_candidate_table.itemSelectionChanged.connect(lambda: setattr(window, "ctf_selected_candidate_key", _selected_candidate_key(window)))
    window.ctf_candidate_table.setStyleSheet("""
        QTableWidget {
            background:#07111d; color:#dff6ff; gridline-color:#173b60;
            border:1px solid #173b60; border-radius:12px; selection-background-color:#0e3a5c;
        }
        QHeaderView::section {
            background:#10243f; color:#84dcff; border:1px solid #173b60; padding:6px;
            font-weight:700;
        }
        QTableWidget::item { padding:6px; }
        QTableWidget::item:selected { background:#0e3a5c; color:#ffffff; }
    """)

    grid = QGridLayout()
    grid.setContentsMargins(0, 0, 0, 0)
    grid.setHorizontalSpacing(14)
    grid.setVerticalSpacing(14)
    grid.addWidget(_shell(window, "Clue Cards", "ctf_clue_cards_view", "CTF clue cards will appear here.", "Text, visual, map, metadata, filename, and region clues.", 360), 0, 0)
    grid.addWidget(_table_shell("Candidate Ranking Table", window.ctf_candidate_table, "Select a candidate, then verify/reject it. GPS > map URL/coordinates > OCR/place labels > visual > filename-only."), 0, 1)
    grid.addWidget(_shell(window, "Search Queries", "ctf_search_queries_view", "Manual search pivots will appear here.", "Generated from OCR phrases, map labels, phone/coordinate/domain clues, and candidate places.", 240), 1, 0)
    grid.addWidget(_shell(window, "Metadata + Privacy Mode", "ctf_metadata_view", "CTF metadata will appear here.", "Solvability score, country/region profile, image existence, and online privacy gate.", 240), 1, 1)
    grid.addWidget(_shell(window, "CTF Writeup Preview", "ctf_writeup_view", "Writeup preview will appear here.", "Exportable markdown narrative for authorised CTF/team training reports.", 320), 2, 0, 1, 2)
    grid.setColumnStretch(0, 1)
    grid.setColumnStretch(1, 1)
    layout.addLayout(grid)
    layout.addStretch(1)
    return widget


def refresh_ctf_geolocator_page(window) -> None:
    records = _records(window)
    if hasattr(window, "ctf_clue_cards_view"):
        window.ctf_clue_cards_view.setHtml(_render_clue_cards(records))
    if hasattr(window, "ctf_candidate_table"):
        _populate_candidate_table(window, records)
    if hasattr(window, "ctf_search_queries_view"):
        window.ctf_search_queries_view.setPlainText(_render_search_queries(records))
    if hasattr(window, "ctf_metadata_view"):
        window.ctf_metadata_view.setPlainText(_render_metadata(records))
    if hasattr(window, "ctf_writeup_view"):
        window.ctf_writeup_view.setPlainText(_render_writeup(records))
